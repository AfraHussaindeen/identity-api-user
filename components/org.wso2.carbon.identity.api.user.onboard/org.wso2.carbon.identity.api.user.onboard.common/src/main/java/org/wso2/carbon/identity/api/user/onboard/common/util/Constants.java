package org.wso2.carbon.identity.api.user.onboard.common.util;

/**
 * Constants for offline user onboarding.
 */
public class Constants {

    private static final String ERROR_PREFIX = "GIL-";

    /**
     * Enum for common server error messages.
     */
    public enum ErrorMessages {
        ERROR_UNABLE_TO_GENERATE_INVITE_LINK(
                "67001",
                "Unable to generate user invite link.",
                "Error occurred while trying to generate invitation link for %s");


        private final String code;
        private final String message;
        private final String description;

        ErrorMessages(String code, String message, String description) {

            this.code = code;
            this.message = message;
            this.description = description;
        }

        public String getCode() {

            return ERROR_PREFIX + code;
        }

        public String getMessage() {

            return message;
        }

        public String getDescription() {

            return description;
        }

        @Override
        public String toString() {

            return code + " | " + message;
        }
    }

}
